
/*
 * 새로 만들려는, '내 클래스'에 대한 클래스 정의예요.
 */
public class MyClass {

	/*
	 * 얘는 '필드'예요.
	 * 풀 네임은 MyClass.number인 듯? - 마우스 갖다 대 봐요
	 */
	public int number;//number는 필드이름이다
	
	/*
	 * 얘는 알다시피 '메서드'예요.
	 * 풀 네임은 MyClass.PrintNumber인 듯? - 마우스 갖다 대 봐요
	 */
	public void PrintNumber()
	{
		System.out.print(number);
	}
	
}
